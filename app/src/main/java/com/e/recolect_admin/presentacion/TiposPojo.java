package com.e.recolect_admin.presentacion;

public class TiposPojo {
    int Domiciliario, Vidrio, Chatarra, Industrial;

    public TiposPojo() {
    }

    public int getDomiciliario() {
        return Domiciliario;
    }

    public void setDomiciliario(int domiciliario) {
        Domiciliario = domiciliario;
    }

    public int getVidrio() {
        return Vidrio;
    }

    public void setVidrio(int vidrio) {
        Vidrio = vidrio;
    }

    public int getChatarra() {
        return Chatarra;
    }

    public void setChatarra(int chatarra) {
        Chatarra = chatarra;
    }

    public int getIndustrial() {
        return Industrial;
    }

    public void setIndustrial(int industrial) {
        Industrial = industrial;
    }
}
